  public static PantallaTablero leerDatos(String ruta) throws IOException, ClassNotFoundException {

    FileInputStream fis = null;
    ObjectInputStream entrada = null;
    PantallaTablero juego = null;

    //Se leen las notas del fichero
    fis = new FileInputStream(ruta);
    entrada = new ObjectInputStream(fis);

    juego = (PantallaTablero) entrada.readObject();
    
    return juego;

  }

